void fft_radix2_recur(unsigned int size, Complex *input, Complex *FFT){
    Complex X0_k;
    Complex X1_k;

    if(size==2){
            c_add(&(FFT[0]),&(input[0]),&(input[1]));
            c_sub(&(FFT[1]),&(input[0]),&(input[1]));
            return;
        }



    //Variable que se usara varias veces
    int newsize=size/2;

    fft_radix2_recur(newsize,input, FFT);

    fft_radix2_recur(newsize,&(input[newsize]),&(FFT[newsize]));

    //W_N esta generado para N=256, hay que escalarlo con el N de la recursión para obtener W_N_rcs y conseguir el ^k



    int k;
    for(k=0;k<newsize;++k){
        X0_k = FFT[k];
        X1_k = FFT[k+newsize];
        c_prod(&X1_k,&(W_N[(N/size)*k]),&(FFT[k+newsize]));
        c_sub(&(FFT[k+newsize]),&X0_k,&X1_k);
        c_add(&(FFT[k]),&X0_k,&X1_k);
    }

    return;

}


void fft_radix2(unsigned int size, Complex *input, Complex *FFT){

    //Variable que se usara varias veces
    //unsigned int newsize=size/2;

    Complex temp;
    unsigned int k=0;
    if (size==8){
        for(k=0; k<size; k++){
            if(R8[k]>k){
                temp=input[k];
                input[k]=input[R8[k]];
                input[R8[k]]=temp;
            }
        }
    }
    else{
        for(k=0; k<size; k++){
            if(R[k]>k){
                temp=input[k];
                input[k]=input[R[k]];
                input[R[k]]=temp;
            }
        }
    }

    fft_radix2_recur(size,input,FFT);

}


/***************************************************************************//**
*   \brief  Función que calcula la magnitud de un vector complejo de frecuencias
*           'fft_freq_vec' entregado por una FFT. Escribe el resultado en
*           'fft_mag_vec'.
*
*   \param  size    : largo del vector a calcular.
*   \param  *fft_freq_vec : puntero a vector complejo de frecuencias
*   \param  *fft_mag_vec  : puntero a vector de flotantes de magnitud.
*
*   \return Void.
*******************************************************************************/
void fft_mag(unsigned int size, Complex *fft_freq_vec, float *fft_mag_vec){
    unsigned int idx;

    for (idx = 0; idx < size; idx++)
        fft_mag_vec[idx] = c_mag(&fft_freq_vec[idx]);
}
